    private void BuscarCurpBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarCurpBotonActionPerformed
        // TODO add your handling code here:
           Connection conexion = null;
        
        try {
            conexion = getConnection();
            ps = conexion.prepareStatement("select * from proyectoSaul.alumnos where curp=?");
            ps.setString(1, BuscarCurpAlumno.getText());
            rs = ps.executeQuery();
            
            if(rs.next()){
                CajaID.setText(String.valueOf(rs.getInt("id_alumno")));
                NombreBajaAlumno.setText(rs.getString("nombre"));
                TelefonoBajaAlumno.setText(rs.getString("telefono"));
                DireccionBajaAlumno.setText(rs.getString("direccion"));
                CurpBajaAlumno.setText(rs.getString("curp"));
                CorreoBajaAlumno.setText(rs.getString("correo"));
                DocumentoBajaAlumno.setText(rs.getString("documento"));
            } else {
                JOptionPane.showMessageDialog(null, "No existe esa persona");
            }
            
            conexion.close();
        }catch(Exception ex){
            System.err.println("Error, "+ex);
        }
    }//GEN-LAST:event_BuscarCurpBotonActionPerformed

