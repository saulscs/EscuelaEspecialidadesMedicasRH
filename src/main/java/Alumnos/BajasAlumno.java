    private void EliminarAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarAlumnoActionPerformed
        // Se muestra dialogo para confirmar la eliminación del alumno
        int desicion = JOptionPane.showConfirmDialog(null, "¿Deseas eliminar este alumno?", "Eliminar", JOptionPane.WARNING_MESSAGE);
        
        if(desicion == JOptionPane.YES_OPTION){ // Si se acepta se ejecuta la eliminación 
            Connection conexion = null;
            
            try{
                conexion = getConnection();
                 ps = conexion.prepareStatement("delete from proyectoSaul.alumnos where curp=?");
                 ps.setString(1, CurpBajaAlumno.getText());
                 
                  String curp = CurpBajaAlumno.getText();
                  
                  int resultado = ps.executeUpdate(); //ejecutamos la eliminacion
                  
                  if(resultado > 0){
                    JOptionPane.showMessageDialog(null, "Registro " + curp + " eliminado correctamente");
                    limpiarCaja();
                    } else {
                        JOptionPane.showMessageDialog(null, "Error al eliminar el registro");
                        limpiarCaja();
                    }
            }catch(Exception ex){
                System.err.println("Error, "+ex);
            }
        }
        
        if(desicion == JOptionPane.NO_OPTION){ // En el no, no se elimina el usuario
            JOptionPane.showMessageDialog(null, "No se elimino el alumno");
        }
        
        if(desicion == JOptionPane.CANCEL_OPTION){ // En el cancel se limpia la pantalla
            JOptionPane.showMessageDialog(null, "Si quiere eliminar un alumno vuelva a hacer la busqueda");
            limpiarCaja();
        }
        
    }//GEN-LAST:event_EliminarAlumnoActionPerformed

